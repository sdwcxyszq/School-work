    private static void getPriceMap(String fileName,
            Map<String, Integer> priceMap) {
    	SimpleReader in =new SimpleReader1L(fileName);
    	String menu;
    	while(!in.atEOS()) {
    		String line = in.nextLine();
    		menu = line.substring(0,line.indexOf(',')-1);
    		int price = Integer.parseInt(line.substring(line.indexOf(',')+1, line.length()));
    		priceMap.add(menu,price);
    	}
    }
